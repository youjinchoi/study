package hackerrank;

import java.util.Scanner;

/*
* https://www.hackerrank.com/challenges/maximum-draws/problem
* */
public class MaximumDraws {

	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		int T = in.nextInt();
		for(int i = 0; i < T; i++){
			int n = in.nextInt();
			System.out.println(n + 1);
		}
	}
}